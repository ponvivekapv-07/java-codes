class MathsOperations{
	public static void main(String[]args)
	{
		//Declaring the variables
		int firstNum=25;
		int secondNum=5;
		System.out.println(firstNum+secondNum); //adding the given variables
		System.out.println(firstNum-secondNum); //subtracting the variable from another one
		System.out.println(firstNum*secondNum); //multiplying the given variables
		System.out.println(firstNum/secondNum); //diving the variable from the another one
	}
}